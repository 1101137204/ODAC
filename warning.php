        <p>刊登人：</p>
        <ul>
            <li>刊登人（運送人）需攜帶由政府機關製發具照片之有效身份証明文件向託運人證明本人身分。</li>
            <li>刊登人（運送人）需攜帶購買的高鐵票券向託運人證明已購買票券。</li>
            <li>刊登人（運送人）需確認託運貨物後，才能進行保管貨物之行為。</li>
            
